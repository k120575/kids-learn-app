def h(t: str) -> str:
    return hashlib.md5(t.encode("utf-8")).hexdigest()[:16]


async def main():
    os.makedirs(OUT, exist_ok=True)
    hashes = []
    baked = 0
    for t in LINES:
        hh = h(t)
        hashes.append(hh)
        path = os.path.join(OUT, hh + ".mp3")
        if not os.path.exists(path):
            await edge_tts.Communicate(t, VOICE, rate=RATE, pitch=PITCH).save(path)
            baked += 1
            print("baked", hh, t)
    # 刪掉測試檔
    test = os.path.join(OUT, "_test.mp3")
    if os.path.exists(test):
        os.remove(test)
    # 輸出 manifest
    with open(MANIFEST, "w", encoding="utf-8") as f:
        f.write("// GENERATED by tool/gen_voice.py — 請勿手動編輯。\n")
        f.write("// 已烤成音檔的台詞 md5(前16) 集合；AudioService 用它決定播音檔或退回 TTS。\n")
        f.write("const Set<String> voiceManifest = <String>{\n")
        for hh in sorted(set(hashes)):
            f.write(f"  '{hh}',\n")
        f.write("};\n")
    print(f"完成：共 {len(set(hashes))} 句，新烤 {baked} 句。")


if __name__ == "__main__":
    asyncio.run(main())
