# 破音字修正：{雜湊/顯示用字 : 實際餵給 TTS 的同音字}。
# 音檔仍存在「顯示字」的雜湊下，App 端照常用顯示字查表播放。
# 「數數點點」的「數」是動詞要唸 ㄕㄨˇ，但 edge-tts 會唸成名詞 ㄕㄨˋ；
# 用同音的「鼠」（ㄕㄨˇ）逼出正確聲調，聽起來與「數數」完全一致。
PRONOUNCE = {
    "數數點點": "鼠鼠點點",
}


def h(t: str) -> str:
    return hashlib.md5(t.encode("utf-8")).hexdigest()[:16]


async def main():
    os.makedirs(OUT, exist_ok=True)
    hashes = []
    baked = 0
    for t in LINES:
        hh = h(t)
        hashes.append(hh)
        path = os.path.join(OUT, hh + ".mp3")
        if not os.path.exists(path):
            src = PRONOUNCE.get(t, t)  # 破音字改餵同音字，雜湊仍用顯示字
            await edge_tts.Communicate(src, VOICE, rate=RATE, pitch=PITCH).save(path)
            baked += 1
