final List<PickRound> zhuyinOnsetBank = _buildZhuyinBank();

List<PickRound> _buildZhuyinBank() {
  final Random rng = Random();
  final List<PickRound> bank = <PickRound>[];
  for (final (String, String, String) w in zhuyinWords) {
    final String correct = w.$3;
    final Set<String> opts = <String>{correct};
    while (opts.length < 4) {
      opts.add(_onsetPool[rng.nextInt(_onsetPool.length)]);
    }
    final List<String> list = opts.toList()..shuffle(rng);
    bank.add(
      PickRound(
        prompt: '『${w.$1}』開頭的注音是哪一個？',
        options: list,
        correctIndex: list.indexOf(correct),
      ),
    );
  }
  return bank;
}

